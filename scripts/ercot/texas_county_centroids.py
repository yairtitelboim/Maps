def get_county_centroid(county_name: str) -> tuple:
    """
    Get centroid coordinates for a Texas county.
    
    Args:
        county_name: County name (case-insensitive, handles variations)
    
    Returns:
        Tuple of (lat, lng) or (None, None) if not found
    """
    if not county_name or pd.isna(county_name):
        return (None, None)
    
    # Normalize county name
    county_clean = str(county_name).strip()
    
    # Try exact match first
    if county_clean in TEXAS_COUNTY_CENTROIDS:
        coords = TEXAS_COUNTY_CENTROIDS[county_clean]
        return (coords['lat'], coords['lng'])
    
    # Try case-insensitive match
    county_lower = county_clean.lower()
    for key, coords in TEXAS_COUNTY_CENTROIDS.items():
        if key.lower() == county_lower:
            return (coords['lat'], coords['lng'])
    
    # Try partial match (handles "Deaf Smith" vs "DeafSmith")
    county_no_spaces = county_clean.replace(' ', '').lower()
    for key, coords in TEXAS_COUNTY_CENTROIDS.items():
        if key.replace(' ', '').lower() == county_no_spaces:
            return (coords['lat'], coords['lng'])
    
    return (None, None)

