/**
 * Load default scenes if no scenes exist
 */
export function loadDefaultScenes() {
  try {
    // Check if scenes already exist
    const existingScenes = localStorage.getItem('transmissionScenes_v1');
    
    if (!existingScenes || JSON.parse(existingScenes).length === 0) {
      
      
      // Load default scenes
      localStorage.setItem('transmissionScenes_v1', JSON.stringify(DEFAULT_SCENES));
      
      

      
      // Dispatch event to notify app
      window.dispatchEvent(new CustomEvent('defaultScenesLoaded', { 
        detail: { sceneCount: DEFAULT_SCENES.length } 
      }));
      
      return true;
    
      return false;
    }
  } catch (error) {
    console.error('âŒ Failed to load default scenes:', error);
    return false;
  }
}

