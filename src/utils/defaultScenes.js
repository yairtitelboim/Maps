/**
 * Check if default scenes are loaded
 */
export function hasDefaultScenes() {
  try {
    const existingScenes = localStorage.getItem('transmissionScenes_v1');
    if (!existingScenes) return false;
    
    const scenes = JSON.parse(existingScenes);
    return scenes.length > 0;
  } catch (error) {
    return false;
  }
}

